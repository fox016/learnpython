<h2>Number Operations</h2>

<p>
Computers carry out commands by writing data to memory, reading data from memory, and performing calculations on that data. We have gone over how to write data to memory by defining variables, but we have yet to learn how to perform calculations on those numbers stored in memory. The type of the variable determines what kinds of calculations can be performed on it, thus this section discusses the types of calculations that can be performed on number types.
</p>
